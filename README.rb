## Install
1.MySqlにて「blog/private/_sql.txt」のCreate文を実行してください。  
2.「blog/private/db.php」のDB接続情報を変更してください。  
3.「blog/global.php」のBASE_PATHには絶対パスを指定ください。  
4.「blog/private/common/config.php」のPUBLIC_PATHをDocumentDoorに合わせて変更してください。  

以上で完了です！  

